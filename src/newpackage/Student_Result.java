    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String roll_num = jTextField1.getText();
        try {
            ClassLoadingAndConnectivity.loadAndConnect();
            Statement st = ClassLoadingAndConnectivity.con.createStatement();
            ResultSet rs = st.executeQuery("select * from Student_Information inner join Student_Results where Student_Information.Roll_Number='" + roll_num + "' and Student_Results.Roll_Num='" + roll_num + "' ");
            if (rs.next()) {
                setVisible(false);
                new Show_Result(roll_num).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Roll Number Does'nt Exsist in database ");
                // setVisible(false);
                new Student_Result().setVisible(true);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection Error");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

